def activity_gear(activity: str, feel: float | None, tmp: float | None) -> str | None:
    if feel is None:
        return None
    if activity == "러닝":
        if feel >= 23: return "기능성 반팔 + 물 필수, 이른 아침·저녁 권장"
        if feel >= 12: return "기능성 반팔/긴팔 + 얇은 바람막이"
        if feel >= 5:  return "긴팔 러닝복 + 바람막이 + 장갑"
        return "방한 러닝복 + 모자·장갑, 준비운동 길게"
    if activity == "서핑" and tmp is not None:
        if tmp >= 24: return "보드숏/스프링수트"
        if tmp >= 18: return "웻수트 3/2mm"
        if tmp >= 12: return "웻수트 4/3mm + 부츠"
        return "웻수트 5/4mm + 부츠·장갑·후드"
    if activity == "등산":
        if feel < 5: return "방한·방풍 자켓 필수 (정상부는 5~10℃ 더 낮음)"
        return "바람막이 챙기기 (정상부는 5~10℃ 더 낮음)"
    if activity == "강아지산책" and feel >= 27:
        return "지면 온도 주의 — 이른 아침·저녁 산책 권장, 물 지참"
    return None
